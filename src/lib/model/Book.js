class Book {

  setId(id) {
    this.id = id;
  }

  getId() {
    return this.id;
  }

  setTitle(title) {
    this.title = title;
  }

  getTitle() {
    return this.title;
  }

  setAuthor(author) {
    this.author = author;
  }

  getAuthor() {
    return this.author;
  }

  setFileName(fileName) {
    this.fileName = fileName;
  }

  getFileName() {
    return this.fileName;
  }

  setReadPosition(readPosition) {
    this.readPosition = readPosition;
  }

  getReadPosition() {
    return this.readPosition;
  }
}
